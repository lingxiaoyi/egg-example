  async show() {
    const ctx = this.ctx;
    ctx.body = await ctx.model.Ads.findByPk(toInt(ctx.params.id));
  }

  async create() {
    const ctx = this.ctx;
    const { projectId, pageId, qidId, ggId } = ctx.request.body;
    const ads = await ctx.model.Ads.create({ projectId, pageId, qidId, ggId });
    ctx.status = 201;
    ctx.body = ads;
  }

  async update() {
    const ctx = this.ctx;
    const id = toInt(ctx.params.id);
    const Ads = await ctx.model.Ads.findByPk(id);
    if (!Ads) {
      ctx.status = 404;
      return;
    }

    const { ggId } = ctx.request.body;
    await Ads.update({ ggId, id });
    ctx.body = Ads;
  }

  async destroy() {
    const ctx = this.ctx;
    const id = toInt(ctx.params.id);
    const Ads = await ctx.model.Ads.findByPk(id);
    if (!Ads) {
      ctx.status = 404;
      return;
    }

    await Ads.destroy();
    ctx.status = 200;
  }
